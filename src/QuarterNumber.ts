/** A quarter number. */
export enum QuarterNumber {
  /**
   * An invalid quarter.
   * @remarks
   * This is required so that there is a default value for the enum.
   */
  Invalid = 0,

  /** First quarter. */
  Q1 = 1,

  /** Second quarter. */
  Q2 = 2,

  /** Third quarter. */
  Q3 = 3,

  /** Fourth quarter. */
  Q4 = 4,
}
